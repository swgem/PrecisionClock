void HAL_SYSTICK_Callback(void)
{
  if (debounce_counter_ms > 0)
  {
    debounce_counter_ms--;
  }
}

void HAL_SPI_RxCpltCallback(SPI_HandleTypeDef *hspi)
{
  unsigned long error;
  uint8_t state_t;

  error = 0;
  state_t = spi_data_r;

  switch (state_t)
  {
    case t_ticks_byte0:
    {
      spi_data_t = (uint8_t)(curr_ticks >> 0);
    }
    break;

    case t_ticks_byte1:
    {
      spi_data_t = (uint8_t)(curr_ticks >> 8);
    }
    break;

    case t_ticks_byte2:
    {
      spi_data_t = (uint8_t)(curr_ticks >> 16);
    }
    break;

    case t_ticks_byte3:
    {
      spi_data_t = (uint8_t)(curr_ticks >> 24);
    }
    break;

    default:
    {
      error = 1;
    }
  }

  if (!error)
  {
    send_data = 1;
  }

  return;
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  uint32_t curr_ticks_aux;

  switch (GPIO_Pin)
  {
    case LORA_INPUT_Pin:
    {
      // Register time when the modem receives a msg
      curr_ticks_aux = __HAL_TIM_GET_COUNTER(&htim3);
  
      if (debounce_counter_ms == 0)
      {
        debounce_counter_ms = DEBOUNCE_TIMEOUT;
        curr_ticks = curr_ticks_aux;
      }
    }
    break;

    case GPS_RST_Pin:
    {
      // Reset timer registers
      __HAL_TIM_SET_PRESCALER(&htim3,0);
      __HAL_TIM_SET_COUNTER(&htim3,0);
      __HAL_TIM_SET_PRESCALER(&htim3,8);
      __HAL_TIM_SET_COUNTER(&htim3,0);
    }
    break;

    default:
    {

    }
    break;   
  }
}

